import { build } from 'esbuild';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const thisFile = fileURLToPath(import.meta.url);
const thisDir = dirname(thisFile);
const workspaceRoot = resolve(thisDir, '..');

const entryFile = resolve(workspaceRoot, 'scripts', 'cubism5-runtime-entry.ts');
const outputFile = resolve(
  workspaceRoot,
  'static',
  'live2d',
  'cubism5',
  'runtime',
  'index.js'
);

await build({
  entryPoints: [entryFile],
  outfile: outputFile,
  bundle: true,
  format: 'esm',
  platform: 'browser',
  target: ['es2020'],
  logLevel: 'info',
  legalComments: 'none',
  sourcemap: false,
  minify: false,
  charset: 'utf8',
  banner: {
    js: '// @ts-nocheck\n// This file is generated by scripts/build-cubism5-runtime.mjs\n'
  }
});

console.log(`Cubism5 runtime bundle generated: ${outputFile}`);
